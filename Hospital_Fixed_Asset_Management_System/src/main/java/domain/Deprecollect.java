package domain;

public class Deprecollect {

	private int id;
	
	private String asset;
	
	private double price;
	
	private int limitage;
	
	private double remain_ratio;
	
	private double deprecost;
	
	private double remain;
	
	private double addepre;
	
	private String depretime;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAsset() {
		return asset;
	}

	public void setAsset(String asset) {
		this.asset = asset;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public int getLimitage() {
		return limitage;
	}

	public void setLimitage(int limitage) {
		this.limitage = limitage;
	}

	public double getRemain_ratio() {
		return remain_ratio;
	}

	public void setRemain_ratio(double remain_ratio) {
		this.remain_ratio = remain_ratio;
	}

	public double getDeprecost() {
		return deprecost;
	}

	public void setDeprecost(double deprecost) {
		this.deprecost = deprecost;
	}

	public double getRemain() {
		return remain;
	}

	public void setRemain(double remain) {
		this.remain = remain;
	}

	public double getAddepre() {
		return addepre;
	}

	public void setAddepre(double addepre) {
		this.addepre = addepre;
	}

	public String getDepretime() {
		return depretime;
	}

	public void setDepretime(String depretime) {
		this.depretime = depretime;
	}
	
	
}
